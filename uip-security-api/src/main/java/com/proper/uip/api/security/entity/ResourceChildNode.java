package com.proper.uip.api.security.entity;

import com.proper.uip.api.security.entity.Resource;



public class ResourceChildNode {

	private String value;
	
	private String key;
	
	
	public ResourceChildNode(Resource resource){
		this.value = resource.getCode();
		this.key = resource.getName();
	}


	public String getValue() {
		return value;
	}


	public void setValue(String value) {
		this.value = value;
	}


	public String getKey() {
		return key;
	}


	public void setKey(String key) {
		this.key = key;
	}

	
}
